public class q6Pattern {
    public static void main(String[] args) {
        for (int i=1;i<=6;i++){
            System.out.print("*"+" ");
        }
        System.out.println();
        for (int i=1;i<=2;i++){
            for (int j=1;j<=2;j++){
                System.out.print("*"+" ");
            }
            System.out.println();
        }
         for (int i=1;i<=6;i++){
            System.out.print("*"+" ");
        }
    }
}
